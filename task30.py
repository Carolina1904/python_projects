"""
Заполните массив элементами арифметической
прогрессии. Её первый элемент, разность и количество
элементов нужно ввести с клавиатуры. Формула для
получения n-го члена прогрессии: a
n
 = a1
 + (n-1) * d.
Каждое число вводится с новой строки.
"""
# Ввод данных с клавиатуры
a1 = float(input("Введите первый элемент прогрессии (a1): "))
d = float(input("Введите разность прогрессии (d): "))
n = int(input("Введите количество элементов прогрессии (n): "))

# Инициализация массива
arithmetic_progression = []

# Заполнение массива
for i in range(1, n + 1):
    an = a1 + (i - 1) * d
    arithmetic_progression.append(an)

# Вывод массива
print("Элементы арифметической прогрессии:")
for element in arithmetic_progression:
    print(element)
